// ---------------------------------------------------------------------------
// Atlas generation
// ---------------------------------------------------------------------------

void Font::Init()
{
    if (s_Atlas)
        return; // already initialised

    // Build a 128x64 RGBA pixel buffer.
    // Char c occupies columns [col*8, col*8+8) and rows [row*8, row*8+8)
    // where col = c % CharsPerRow, row = c / CharsPerRow.
    constexpr int W = AtlasWidth;
    constexpr int H = AtlasHeight;
    uint8_t pixels[W * H * 4] = {};

    for (int c = 32; c <= 126; c++)
    {
        const uint8_t* glyph = s_GlyphData[c - 32];
        int col = c % CharsPerRow;
        int row = c / CharsPerRow;
        int px  = col * CharWidth;
        int py  = row * CharHeight;

        for (int gy = 0; gy < CharHeight; gy++)
        {
            uint8_t row_bits = glyph[gy];
            for (int gx = 0; gx < CharWidth; gx++)
            {
                bool set = (row_bits >> (7 - gx)) & 1;
                // The texture is uploaded with stbi flip-on-load = 1 elsewhere,
                // but here we write directly so we flip Y so row 0 is the top.
                int atlasX = px + gx;
                int atlasY = (H - 1) - (py + gy); // flip Y for OpenGL
                int idx = (atlasY * W + atlasX) * 4;
                pixels[idx + 0] = set ? 255 : 0;
                pixels[idx + 1] = set ? 255 : 0;
                pixels[idx + 2] = set ? 255 : 0;
                pixels[idx + 3] = set ? 255 : 0;
            }
        }
    }

    s_Atlas = Texture2D::CreateEmpty(W, H);
    s_Atlas->SetData(pixels, sizeof(pixels));
}

void Font::Shutdown()
{
    s_Atlas.reset();
}

// ---------------------------------------------------------------------------
// Drawing helpers
// ---------------------------------------------------------------------------

static void GetCharUV(int c, glm::vec2& uvMin, glm::vec2& uvMax)
{
    if (c < 32 || c > 126)
        c = '?';

    int col = c % Font::CharsPerRow;
    int row = c / Font::CharsPerRow;

    // UV in [0,1]: x goes left-to-right, y goes bottom-to-top in OpenGL
    float u0 = (float)(col * Font::CharWidth)  / Font::AtlasWidth;
    float u1 = u0 + (float)Font::CharWidth     / Font::AtlasWidth;

    // Because we flipped Y when writing the atlas we need to reverse here too
    float v1 = 1.0f - (float)(row * Font::CharHeight)     / Font::AtlasHeight;
    float v0 = v1   - (float)Font::CharHeight             / Font::AtlasHeight;

    uvMin = { u0, v0 };
    uvMax = { u1, v1 };
}

void Font::DrawText(const std::string& text,
                    const glm::vec3&   position,
                    float              charSize,
                    const glm::vec4&   color)
{
    if (!s_Atlas)
        return;

    float xOffset = 0.0f;
    for (char ch : text)
    {
        if (ch == '\n')
            break; // single-line; use DrawTextMultiline for wrapping

        glm::vec2 uvMin, uvMax;
        GetCharUV(static_cast<int>(ch), uvMin, uvMax);

        // Each character quad is centred at its cell, size = charSize × charSize.
        // We place the bottom-left corner at (position.x + xOffset, position.y).
        glm::vec3 centre = position + glm::vec3(xOffset + charSize * 0.5f,
                                                charSize * 0.5f,
                                                0.0f);
        glm::mat4 transform = glm::translate(glm::mat4(1.0f), centre)
                            * glm::scale(glm::mat4(1.0f), { charSize, charSize, 1.0f });

        Renderer2D::DrawQuad(transform, s_Atlas, uvMin, uvMax, color);
        xOffset += charSize;
    }
}

void Font::DrawTextMultiline(const std::string& text,
                              const glm::vec3&   position,
                              float              charSize,
                              float              lineSpacing,
                              const glm::vec4&   color)
{
    if (!s_Atlas)
        return;

    std::istringstream stream(text);
    std::string line;
    int lineIndex = 0;
    while (std::getline(stream, line))
    {
        glm::vec3 linePos = position - glm::vec3(0.0f, lineIndex * charSize * lineSpacing, 0.0f);
        DrawText(line, linePos, charSize, color);
        lineIndex++;
    }
}

float Font::GetTextWidth(const std::string& text, float charSize)
{
    // Count characters up to first newline
    float count = 0;
    for (char c : text)
    {
        if (c == '\n')
            break;
        count += 1.0f;
    }
    return count * charSize;
}

